function rates( u, y₁=0.0, y₂=0.0, μ₁=0.0, μ₂=0.0, a₁=0.0, a₂=0.0, b₁=0.0, b₂=0.0, n=1)
	return [ a₁/(1+(y₁*u[4])^n) - μ₁*u[1],
			 b₁*u[1] - μ₂*u[2],
			 a₂/(1+(y₂*u[2])^n) - μ₁*u[3],
			 b₂*u[3] - μ₂*u[4] ]
end

function jacobian( u,p, θ₁=0.0, θ₂=0.0, θ₃=0.0)
	return [[ θ₁ .+ 3 .*θ₂.*u[1].^2, 1.0 ] [ 0.0, -1.0 ]]
end
